    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       patient_id= result_patient_id.getText();
       try {
           
            
            String sql = "select * from patient_info where ID= '" + patient_id + "' ";
            rs = st.executeQuery(sql);
            while (rs.next()) {
                String names = rs.getString("NAME");
                result_patient_name.setText(names);
                String age = rs.getString("AGE");
                result_age.setText(age);
            }
            System.out.println(""+patient_id);
            String sql1 = "select * from diagnosis_info where ID= '" + patient_id + "' ";
            rs = st.executeQuery(sql1);
            while (rs.next()) {
                String results = rs.getString("Result");
                double num = Double.parseDouble(results);
                if(num>0.0 && num<=0.13)
                result.setText("Mild( "+results+" )");
                else if(num>0.13 && num<=0.23)
                 result.setText("Moderate( "+results+" )");
                else if(num>0.23 && num<=0.3)
                 result.setText("Severe( "+results+" )");
                
            }
           

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Database Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

